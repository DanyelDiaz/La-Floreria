    public void resive( int id,String nombre, float precio){
        int x=0;
        for(int i=0;i<9;i++)
        {     
            if(jTable1.getValueAt( i, 3) != null){
                if(id == (int)jTable1.getValueAt( i, 3)){
                    x = (Integer)jTable1.getValueAt( i, 0);
                    jTable1.setValueAt(x+1, i, 0);
                    jTable1.setValueAt(x*precio, i, 2);
                    break;
                }
            }
            else
            {
                jTable1.setValueAt(1, a, 0);
                jTable1.setValueAt(nombre, a, 1);
                jTable1.setValueAt(precio, a, 2);
                jTable1.setValueAt(id, a, 3);
                a++;
                break;
            }
        }
    }
